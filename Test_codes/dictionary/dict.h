class dictionary
{
  public:
    String Word;
    int sizeOfWord;

    dictionary(){
      Word = "";
      sizeOfWord = 0;
    }

    void addChar(String w){
      Word += w;
      sizeOfWord++;
      Serial.println(Word);
    }

    void ClearWord(){
      Word = "";
    }

    void deleteRecentChar(){
       
       Word.remove(sizeOfWord-1);
       sizeOfWord--;
       
    }

    void displayWord(){
      Serial.print(Word);
    }
    
};
